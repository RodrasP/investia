db.serialize(() => {
  console.log('Synchronizing default translations to database...');
  
  const keys = Object.keys(defaultTranslations.es);
  
  keys.forEach(key => {
    const es = defaultTranslations.es[key];
    const en = defaultTranslations.en[key];
    
    // Check if key exists, if not insert it
    db.get('SELECT key FROM translations WHERE key = ?', [key], (err, row) => {
      if (err) return console.error(err);
      
      if (!row) {
        db.run('INSERT INTO translations (key, es, en) VALUES (?, ?, ?)', [key, es, en], (err) => {
          if (err) console.error(`Error inserting key ${key}:`, err);
          else console.log(`Inserted key: ${key}`);
        });
      } else {
        console.log(`Key already exists: ${key}`);
      }
    });
  });
});
